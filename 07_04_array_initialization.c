#include <stdio.h>

int main()
{

    int a[] = {33, 44, 55};

    printf("The value of array a[0] is %d\n", a[0]);
    printf("The value of array a[1] is %d\n", a[1]);
    printf("The value of array a[2] is %d\n", a[2]);

    float b[] = {363.3, 474.4, 545.5};

    printf("The value of array b[0] is %f\n", b[0]);
    printf("The value of array b[1] is %f\n", b[1]);
    printf("The value of array b[2] is %f\n", b[2]);
    return 0;
}