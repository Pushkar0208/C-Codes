#include <stdio.h>

int main()
{

    int a = 9, b = 7;

    printf(" The value of a + b is : %d\n", a + b);
    printf(" The value of a - b is : %d\n", a - b);
    printf(" The value of a * b is : %d\n", a * b);
    printf(" The value of a / b is : %d\n", a / b);
    printf(" The value of a %% b is : %d\n", a % b);
    
    return 0;
}
